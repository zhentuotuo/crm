package com.jykj.pojo;

import java.util.Date;

public class Employlog {
    private String todaywork;
    private String tomowork;
    private Date datetimes;

    public String getTodaywork() {
        return todaywork;
    }

    public void setTodaywork(String todaywork) {
        this.todaywork = todaywork;
    }

    public String getTomowork() {
        return tomowork;
    }

    public void setTomowork(String tomowork) {
        this.tomowork = tomowork;
    }

    public Date getDatetimes() {
        return datetimes;
    }

    public void setDatetimes(Date datetimes) {
        this.datetimes = datetimes;
    }
}
