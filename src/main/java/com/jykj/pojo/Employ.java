package com.jykj.pojo;

public class Employ {
    private int id;
    private String EmplName;
    private String EmplPwd;
    private int EmplDepa;
    private int EmplRegion;
    private int EmplPost;
    private String EmplPhone;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmplName() {
        return EmplName;
    }

    public void setEmplName(String emplName) {
        EmplName = emplName;
    }

    public String getEmplPwd() {
        return EmplPwd;
    }

    public void setEmplPwd(String emplPwd) {
        EmplPwd = emplPwd;
    }

    public int getEmplDepa() {
        return EmplDepa;
    }

    public void setEmplDepa(int emplDepa) {
        EmplDepa = emplDepa;
    }

    public int getEmplRegion() {
        return EmplRegion;
    }

    public void setEmplRegion(int emplRegion) {
        EmplRegion = emplRegion;
    }

    public int getEmplPost() {
        return EmplPost;
    }

    public void setEmplPost(int emplPost) {
        EmplPost = emplPost;
    }

    public String getEmplPhone() {
        return EmplPhone;
    }

    public void setEmplPhone(String emplPhone) {
        EmplPhone = emplPhone;
    }
}
