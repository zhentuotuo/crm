package com.jykj.pojo;

public class EmployDetails {
    private int id;
    private String uname;
    private String uphone;
    private int educ;
    private int mari;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getUphone() {
        return uphone;
    }

    public void setUphone(String uphone) {
        this.uphone = uphone;
    }

    public int getEduc() {
        return educ;
    }

    public void setEduc(int educ) {
        this.educ = educ;
    }

    public int getMari() {
        return mari;
    }

    public void setMari(int mari) {
        this.mari = mari;
    }
}
